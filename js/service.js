
// for 1st service popup 

document.getElementById('popupBtn1').addEventListener('click', function(event) {
  event.stopPropagation(); // Prevent propagation of click event to the background
  document.getElementById('popupContent-service1-sec2').style.display = 'block';
});

document.getElementById('closeBtn-service1-sec2').addEventListener('click', function() {
  document.getElementById('popupContent-service1-sec2').style.display = 'none';
});

// Prevent propagation of click events to the background
document.getElementById('popupContent-service1-sec2').addEventListener('click', function(event) {
  event.stopPropagation();
});

// Close the popup if clicked outside the popup content
window.addEventListener('click', function(event) {
  var popupContent = document.getElementById('popupContent-service1-sec2');
  if (event.target !== popupContent && !popupContent.contains(event.target)) {
    popupContent.style.display = 'none';
  }
});


